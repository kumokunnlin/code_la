#include<bits/stdc++.h>
using namespace std;

int main(){
	int h[2],m[2],s[2];
	string e,d;
	cin>>e>>d;
	h[0]=(e[0]-48)*10+e[1]-48;
	h[1]=(d[0]-48)*10+d[1]-48;
	m[0]=(e[3]-48)*10+e[4]-48;
	m[1]=(d[3]-48)*10+d[4]-48;
	s[0]=(e[6]-48)*10+e[7]-48;
	s[1]=(d[6]-48)*10+d[7]-48;
	if(h[0]*3600+m[0]*60+s[0]<h[1]*3600+m[1]*60+s[1]){
		if(s[1]<s[0]){
			s[1]+=60;
			m[1]-=1;
		}
		if(m[1]<m[0]){
			m[1]+=60;
			h[1]-=1;
		}
		if(h[1]-h[0]<10){
			cout<<0;
		}
		cout<<h[1]-h[0]<<':';
		if(m[1]-m[0]<10){
			cout<<0;
		}
		cout<<m[1]-m[0]<<':';
		if(s[1]-s[0]<10){
			cout<<0;
		}
		cout<<s[1]-s[0]<<endl;
	}else{
		h[1]+=24;
		if(s[1]<s[0]){
			s[1]+=60;
			m[1]-=1;
		}
		if(m[1]<m[0]){
			m[1]+=60;
			h[1]-=1;
		}
		if(h[1]-h[0]<10){
			cout<<0;
		}
		cout<<h[1]-h[0]<<':';
		if(m[1]-m[0]<10){
			cout<<0;
		}
		cout<<m[1]-m[0]<<':';
		if(s[1]-s[0]<10){
			cout<<0;
		}
		cout<<s[1]-s[0]<<endl;
	}
}
